package com.hz.pojo;


public class FoPiletype {

  private long piletypeId;
  private String piletypeName;
  private java.sql.Timestamp pileCreationtime;


  public long getPiletypeId() {
    return piletypeId;
  }

  public void setPiletypeId(long piletypeId) {
    this.piletypeId = piletypeId;
  }


  public String getPiletypeName() {
    return piletypeName;
  }

  public void setPiletypeName(String piletypeName) {
    this.piletypeName = piletypeName;
  }


  public java.sql.Timestamp getPileCreationtime() {
    return pileCreationtime;
  }

  public void setPileCreationtime(java.sql.Timestamp pileCreationtime) {
    this.pileCreationtime = pileCreationtime;
  }

}
