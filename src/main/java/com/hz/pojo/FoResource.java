package com.hz.pojo;


public class FoResource {

  private long resourceId;
  private String resourceName;
  private String resourceUrl;
  private String resourceIocn;
  private String resourceSorts;
  private long resourcePid;


  public long getResourceId() {
    return resourceId;
  }

  public void setResourceId(long resourceId) {
    this.resourceId = resourceId;
  }


  public String getResourceName() {
    return resourceName;
  }

  public void setResourceName(String resourceName) {
    this.resourceName = resourceName;
  }


  public String getResourceUrl() {
    return resourceUrl;
  }

  public void setResourceUrl(String resourceUrl) {
    this.resourceUrl = resourceUrl;
  }


  public String getResourceIocn() {
    return resourceIocn;
  }

  public void setResourceIocn(String resourceIocn) {
    this.resourceIocn = resourceIocn;
  }


  public String getResourceSorts() {
    return resourceSorts;
  }

  public void setResourceSorts(String resourceSorts) {
    this.resourceSorts = resourceSorts;
  }


  public long getResourcePid() {
    return resourcePid;
  }

  public void setResourcePid(long resourcePid) {
    this.resourcePid = resourcePid;
  }

}
