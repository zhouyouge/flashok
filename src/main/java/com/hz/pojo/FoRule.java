package com.hz.pojo;


public class FoRule {

  private long ruleId;
  private String chargingRules;
  private long piletypeId;


  public long getRuleId() {
    return ruleId;
  }

  public void setRuleId(long ruleId) {
    this.ruleId = ruleId;
  }


  public String getChargingRules() {
    return chargingRules;
  }

  public void setChargingRules(String chargingRules) {
    this.chargingRules = chargingRules;
  }


  public long getPiletypeId() {
    return piletypeId;
  }

  public void setPiletypeId(long piletypeId) {
    this.piletypeId = piletypeId;
  }

}
